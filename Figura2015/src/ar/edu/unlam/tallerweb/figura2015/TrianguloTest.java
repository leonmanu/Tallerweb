package ar.edu.unlam.tallerweb.figura2015;

import org.junit.Assert;
import org.junit.Test;


public class TrianguloTest {
	
	@Test
	public void testQueDevuelvaAreaDelTriangulo(){
		
		Triangulo miTriangulo = new Triangulo(2.0,1.0,1.0);
		miTriangulo.area();
		Assert.assertTrue("",miTriangulo.area()==0.0);
	}
	
	@Test
	public void testQueDevuelvaPerimetroDelTriangulo(){
		
		Triangulo miTriangulo = new Triangulo(2.0,1.0,1.0);
		miTriangulo.perimetro();
		Assert.assertTrue("",miTriangulo.perimetro()==4.0);
	}

}
